def normalize_locale(locale: str | None) -> str:
    if not locale:
        return "en"
    normalized = locale.replace("_", "-").strip().lower()
    if not normalized:
        return "en"
    language = normalized.split("-", 1)[0]
    if language in SUPPORTED_LANGUAGES:
        return language
    return "en"


def translate(locale: str | None, key: str, **kwargs: object) -> str:
    language = normalize_locale(locale)
    template = STRINGS.get(language, STRINGS["en"]).get(key) or STRINGS["en"][key]
    if kwargs:
        return template.format(**kwargs)
    return template


def format_month_day(value: date, locale: str | None) -> str:
    language = normalize_locale(locale)
    month = MONTH_NAMES.get(language, MONTH_NAMES["en"])[value.month - 1]
    if language in {"en", "de"}:
        return f"{month} {value.day}"
    return f"{value.day} {month}"
